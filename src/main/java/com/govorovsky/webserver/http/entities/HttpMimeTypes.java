package com.govorovsky.webserver.http.entities;

import java.util.HashMap;
import java.util.Map;

/**
 * Created by Andrew Govorovsky on 11.06.14
 */
public class HttpMimeTypes {
    private static final Map<String, String> mimes = new HashMap<String, String>() {{
        put("css", "text/css");
        put("htm", "text/html");
        put("html", "text/html");
        put("xml", "text/xml");
        put("java", "text/x-java-source, text/java");
        put("md", "text/plain");
        put("txt", "text/plain");
        put("asc", "text/plain");
        put("gif", "image/gif");
        put("jpg", "image/jpeg");
        put("jpeg", "image/jpeg");
        put("png", "image/png");
        put("mp3", "audio/mpeg");
        put("m3u", "audio/mpeg-url");
        put("mp4", "video/mp4");
        put("ogv", "video/ogg");
        put("flv", "video/x-flv");
        put("mov", "video/quicktime");
        put("swf", "application/x-shockwave-flash");
        put("js", "application/javascript");
        put("pdf", "application/pdf");
        put("doc", "application/msword");
        put("ogg", "application/x-ogg");
        put("zip", "application/octet-stream");
        put("exe", "application/octet-stream");
        put("class", "application/octet-stream");
    }};

    private static final String DEFAULT_TYPE = "application/octet-stream";

    public static String getMimeType(String extension) {
        String mime_type = mimes.get(extension);
        return mime_type != null ? mime_type : DEFAULT_TYPE;
    }
}
